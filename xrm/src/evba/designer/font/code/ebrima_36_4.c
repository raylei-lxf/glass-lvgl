static int binarySearch(const uint16_t *orderSeq, int seqLength, uint16_t keyData)
{
	int low = 0, mid, high = seqLength - 1;

	while (low <= high)
	{
		mid = (low + high)>>1;
		if(keyData == orderSeq[mid])
		{
			return mid;
		}
		else if (keyData < orderSeq[mid])
		{
			high = mid - 1;
		}
		else if (keyData > orderSeq[mid])
		{
			low = mid + 1;
		}
	}

	return -1;
}

static const uint8_t * __user_font_get_bitmap(const lv_font_t * font, uint32_t unicode_letter)
{
	lv_font_fmt_txt_dsc_t * fdsc = (lv_font_fmt_txt_dsc_t *) font->dsc;
	int index = -1;

	if(unicode_letter == fdsc->last_letter)
	{
		const lv_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[fdsc->last_glyph_id];
		return &fdsc->glyph_bitmap[gdsc->bitmap_index];
	}

	if(unicode_letter < fdsc->cmaps[0].range_start)
	{
		return NULL;
	}

	index = binarySearch(fdsc->cmaps[0].unicode_list, fdsc->cmaps[0].range_length, unicode_letter);
	if( index != -1 )
	{
		const lv_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[index];
		return &fdsc->glyph_bitmap[gdsc->bitmap_index];
	}

	return NULL;
}

static bool __user_font_get_glyph_dsc(const lv_font_t * font, lv_font_glyph_dsc_t * dsc_out, uint32_t unicode_letter, uint32_t unicode_letter_next)
{
	lv_font_fmt_txt_dsc_t * fdsc = (lv_font_fmt_txt_dsc_t *) font->dsc;
	int index = -1;

	if(unicode_letter == fdsc->last_letter)
	{
		const lv_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[fdsc->last_glyph_id];
		dsc_out->adv_w = gdsc->adv_w;
		dsc_out->box_h = gdsc->box_h;
		dsc_out->box_w = gdsc->box_w;
		dsc_out->ofs_x = gdsc->ofs_x;
		dsc_out->ofs_y = gdsc->ofs_y;
		dsc_out->bpp   = fdsc->bpp;
		return true;
	}

	if(unicode_letter < fdsc->cmaps[0].range_start) return false;

	index = binarySearch(fdsc->cmaps[0].unicode_list, fdsc->cmaps[0].range_length, unicode_letter);
	if( index != -1 )
	{
		const lv_font_fmt_txt_glyph_dsc_t * gdsc = &fdsc->glyph_dsc[index];
		dsc_out->adv_w = gdsc->adv_w;
		dsc_out->box_h = gdsc->box_h;
		dsc_out->box_w = gdsc->box_w;
		dsc_out->ofs_x = gdsc->ofs_x;
		dsc_out->ofs_y = gdsc->ofs_y;
		dsc_out->bpp   = fdsc->bpp;

		fdsc->last_glyph_id = index;
		fdsc->last_letter = unicode_letter;
		return true;
	}

	return false;
}

